export function translate(text: string, locale: Locale) {
  if (locale === "en") return text;
  return zh[text] ?? text;
}

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("zh");

  function setLocale(next: Locale) {
    setLocaleState(next);
  }

  const value = useMemo(() => ({ locale, setLocale, t: (text: string) => translate(text, locale) }), [locale]);
  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const value = useContext(LocaleContext);
  if (!value) throw new Error("useLocale must be used inside LocaleProvider");
  return value;
}

export function LocaleToggle() {
  const { locale, setLocale } = useLocale();
  return (
    <div className="inline-flex rounded-md border border-app-border bg-app-surface p-1 text-xs font-semibold">
      <button className={`rounded px-2 py-1 ${locale === "zh" ? "bg-brand-primary text-white" : "text-app-text-secondary"}`} type="button" onClick={() => setLocale("zh")}>中文</button>
      <button className={`rounded px-2 py-1 ${locale === "en" ? "bg-brand-primary text-white" : "text-app-text-secondary"}`} type="button" onClick={() => setLocale("en")}>EN</button>
    </div>
  );
}
